#include "3-calc.h"

/**
  * op_add - returns the sum of 2 integers
  * @a: first integer
  * @b: second integer
  *
  * Return: The sum
  */

int op_add(int a, int b)
{
	return (a + b);
}

/**
  * op_sub - returns the difference of 2 integers
  * @a: first integer
  * @b: second integer
  *
  * Return: The difference
  */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
  * op_mul - returns the product of 2 integers
  * @a: first integer
  * @b: second integer
  *
  * Return: The product
  */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
  * op_div - returns the quotient of 2 integers
  * @a: first integer
  * @b: second integer
  *
  * Return: The quotient
  */

int op_div(int a, int b)
{
	return (a / b);
}

/**
  * op_mod - returns the remainder of 2 integers
  * @a: first integer
  * @b: second integer
  *
  * Return: The remainder
  */

int op_mod(int a, int b)
{
	return (a % b);
}
